package model;

public class CrownBonus {
	private int requiredEarning;
	private int requriedDuration;
	private int bonusAmount;

	public int getRequiredEarning() {
		return requiredEarning;
	}

	public void setRequiredEarning(int requiredEarning) {
		this.requiredEarning = requiredEarning;
	}

	public int getRequriedDuration() {
		return requriedDuration;
	}

	public void setRequriedDuration(int requriedDuration) {
		this.requriedDuration = requriedDuration;
	}

	public int getBonusAmount() {
		return bonusAmount;
	}

	public void setBonusAmount(int bonusAmount) {
		this.bonusAmount = bonusAmount;
	}
}
