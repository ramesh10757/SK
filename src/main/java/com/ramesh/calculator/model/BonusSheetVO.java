package com.ramesh.calculator.model;

public class BonusSheetVO {
	private int minEarning;
	private int maxEarning;
	private int bonusAmount;

	public int getMinEarning() {
		return minEarning;
	}

	public void setMinEarning(int minEarning) {
		this.minEarning = minEarning;
	}

	public int getMaxEarning() {
		return maxEarning;
	}

	public void setMaxEarning(int maxEarning) {
		this.maxEarning = maxEarning;
	}

	public int getBonusAmount() {
		return bonusAmount;
	}

	public void setBonusAmount(int bonusAmount) {
		this.bonusAmount = bonusAmount;
	}
}
