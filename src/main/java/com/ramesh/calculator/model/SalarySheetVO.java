package com.ramesh.calculator.model;

public class SalarySheetVO {
	private int gemsRequirement;
	private int gemsSharing;
	private int basicSalary;

	public int getGemsRequirement() {
		return gemsRequirement;
	}

	public void setGemsRequirement(int gemsRequirement) {
		this.gemsRequirement = gemsRequirement;
	}

	public int getGemsSharing() {
		return gemsSharing;
	}

	public void setGemsSharing(int gemsSharing) {
		this.gemsSharing = gemsSharing;
	}

	public int getBasicSalary() {
		return basicSalary;
	}

	public void setBasicSalary(int basicSalary) {
		this.basicSalary = basicSalary;
	}

}
